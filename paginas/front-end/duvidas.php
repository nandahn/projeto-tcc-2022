                        <img src="">
                        <p id="conteudo">
                        ➥ A ingestão de água ou suco de frutas naturais é uma maneira de preparar o organismo para doação de sangue. 
                        Recomenda-se que a hidratação seja iniciada no dia anterior à doação. A orientação integra os critérios que 
                        antecedem a coleta de sangue e tem como finalidade garantir a qualidade e segurança no procedimento.Após a 
                        coleta, o doador recebe um lanche e, mais uma vez, é orientado a ingerir bastante líquido ao longo do dia.
                        </p> 
